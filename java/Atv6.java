public class Atv6 {
    public static void main(String[] args) {
        // Imprimir os números de 1 a 20 um abaixo do outro
        System.out.println("Imprimindo os números de 1 a 20 um abaixo do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }

        // Imprimir os números de 1 a 20 um ao lado do outro
        System.out.println("\nImprimindo os números de 1 a 20 um ao lado do outro:");
        for (int i = 1; i <= 20; i++) {
            System.out.print(i + " ");
        }
    }
}

